CREATE TABLE net_savedlessons (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id INTEGER REFERENCES net_users(id) ON DELETE CASCADE NOT NULL,
    title TEXT NOT NULL,
    date TEXT NOT NULL,
    day TEXT NOT NULL,
    duration TEXT NOT NULL,
    classlevel TEXT NOT NULL,
    period TEXT NOT NULL,
    topic TEXT NOT NULL,
    goal TEXT NOT NULL,
    class_size INTEGER NOT NULL,
    objective_one TEXT NOT NULL,
    objective_two TEXT,
    objective_three TEXT,
    materials TEXT NOT NULL,
    warmup_id INTEGER REFERENCES net_activities(id) NOT NULL,
    presentation_one_id INTEGER REFERENCES net_activities(id) NOT NULL,
    presentation_two_id INTEGER REFERENCES net_activities(id) NOT NULL,
    practice_one_id INTEGER REFERENCES net_activities(id) NOT NULL,
    practice_two_id INTEGER REFERENCES net_activities(id) NOT NULL,
    practice_three_id INTEGER REFERENCES net_activities(id) NOT NULL,
    product_one_id INTEGER REFERENCES net_activities(id) NOT NULL,
    product_two_id INTEGER REFERENCES net_activities(id) NOT NULL,
    cooldown_id INTEGER REFERENCES net_activities(id) NOT NULL,
    reflection_one TEXT NOT NULL,
    reflection_two TEXT,
    reflection_three TEXT,
    date_created TIMESTAMP DEFAULT now() NOT NULL,
    date_modified TIMESTAMP
);